first_number = int(input("Ingrese el primer número: "))
second_number = int(input("Ingrese el segundo número: "))

addition = first_number + second_number
subtraction = first_number - second_number
multiplication = first_number * second_number
exponentiation = first_number ** second_number

print(f"La suma de {first_number} y {second_number} es de: {addition}")
print(f"La resta de {first_number} y {second_number} es de: {subtraction}")
print(
    f"La multiplicación de {first_number} y {second_number} es de: {multiplication}")
print(
    f"La potenciación de {first_number} y {second_number} es de: {exponentiation}")
