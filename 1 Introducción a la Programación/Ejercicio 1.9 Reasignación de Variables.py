number = int(input("Introduzca un número: "))

print(f"El número introducido es: {number}")
number = number + 7
print(f"Al sumar 7 unidades el resultado es: {number}")
number = number * 2
print(f"Al multiplicar el valor por 2 el resultado es: {number}")
number = number - 4
print(f"Al restar 4 unidades el resultado es: {number}")
number = number // 3
print(f"El resultado de la división entera entre 3 es: {number}")
